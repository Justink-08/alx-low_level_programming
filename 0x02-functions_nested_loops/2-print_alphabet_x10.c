#include "main.h"

/*
 * main -  a function that prints 10 times the alphabet
 *
 * description: A fuction to multiply alphabets by 10
 *
 * return: Always (0) success
 *
 * */

void print_alphabet_x10(void)
{

	int x, x0;

	x0 = 0;

	while (x0 = 10)
	{
		for (x ='a'; x <='z'; x++)
		{
			_putchar(x);
		}
		
		x0++;
		
		_putchar('\n');}
}
